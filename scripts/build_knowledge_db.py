def main():
    con = sqlite3.connect(DB); con.executescript(SCHEMA); cur = con.cursor()
    cur.execute('INSERT INTO categories(code,name) VALUES(?,?)', CATEGORY)
    cat_id = cur.lastrowid
    src = 'catalogs/klapany/Воздушные клапаны.pdf'
    fam_id = {}
    for mark,title,sub,page,status in FAMILIES:
        cur.execute('INSERT INTO families(category_id,mark,title,subgroup,catalog_page,source_file,status) VALUES(?,?,?,?,?,?,?)',
                    (cat_id,mark,title,sub,page,src,status))
        fam_id[mark]=cur.lastrowid
    for mark,rows in PARAMS.items():
        for grp,key,val,unit in rows:
            cur.execute('INSERT INTO params(family_id,model_code,grp,key,value,unit) VALUES(?,?,?,?,?,?)',
                        (fam_id[mark],None,grp,key,val,unit))
    for ck,al,unit,scope in PARAM_DICT:
        cur.execute('INSERT INTO param_dict(canonical_key,aliases,unit,scope) VALUES(?,?,?,?)',
                    (ck,json.dumps(al,ensure_ascii=False),unit,scope))
    con.commit()
    # Сводка
    print('БД:', os.path.relpath(DB))
    print('Категорий :', cur.execute('SELECT COUNT(*) FROM categories').fetchone()[0])
    print('Марок     :', cur.execute('SELECT COUNT(*) FROM families').fetchone()[0])
    print('Характеристик:', cur.execute('SELECT COUNT(*) FROM params').fetchone()[0])
    for st in ('full','partial','stub'):
        n=cur.execute('SELECT COUNT(*) FROM families WHERE status=?',(st,)).fetchone()[0]
        print(f'  status={st}: {n}')
    con.close()

if __name__=='__main__':
    main()
