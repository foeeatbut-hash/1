// --- Токенизация и подбор по смыслу ---
const STOP = new Set(['как', 'что', 'где', 'это', 'для', 'чего', 'мне', 'нужно', 'нужен', 'можно', 'сделать', 'тут', 'здесь', 'или', 'и', 'в', 'на', 'по', 'про', 'a', 'the']);

function toks(s: string): string[] {
  return s.toLowerCase().replace(/[^а-яёa-z0-9\s-]/gi, ' ').split(/\s+/).filter(w => w.length >= 3 && !STOP.has(w));
}

// Оценка соответствия тура запросу: совпадения ключевых слов + пересечение
// со списком типичных формулировок (как в «100 вариаций»).
function scoreTour(tour: Tour, userText: string, userToks: string[]): number {
  const t = userText.toLowerCase();
  let score = 0;
  for (const kw of tour.keywords) {
    if (t.includes(kw)) score += 1;
  }
  let bestPhrase = 0;
  for (const q of tour.questions) {
    const qToks = toks(q);
    let overlap = 0;
    for (const ut of userToks) {
      if (qToks.some(qt => qt === ut || qt.includes(ut) || ut.includes(qt))) overlap++;
    }
    if (overlap > bestPhrase) bestPhrase = overlap;
  }
  return score + bestPhrase * 1.5;
}

export interface TourMatch { tour: Tour; score: number; }

export function findBestTour(text: string): TourMatch | null {
  const userToks = toks(text);
  let best: Tour | null = null;
  let bestScore = 0;
  for (const tour of TOURS) {
    const s = scoreTour(tour, text, userToks);
    if (s > bestScore) { bestScore = s; best = tour; }
  }
  return best ? { tour: best, score: bestScore } : null;
}

// Обратная совместимость: вернуть тур, если уверенность достаточная
export function findTourByText(text: string): Tour | null {
  const m = findBestTour(text);
  return m && m.score >= 1.5 ? m.tour : null;
}
