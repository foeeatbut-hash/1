// Подбор демонстрации: считаем совпадения ключевых слов; при равенстве
// предпочитаем тур, чей идентификатор-сущность упомянут явно.
export function findTourByText(text: string): Tour | null {
  const t = text.toLowerCase();
  let best: Tour | null = null;
  let bestScore = 0;
  for (const tour of TOURS) {
    let score = 0;
    for (const kw of tour.keywords) {
      if (t.includes(kw)) score++;
    }
    if (score > bestScore) {
      bestScore = score;
      best = tour;
    }
  }
  return bestScore > 0 ? best : null;
}
